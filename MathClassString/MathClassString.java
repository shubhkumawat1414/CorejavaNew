package MathClassString;

public class MathClassString {
	
	public static void main(String[] args) {
		
		int a = 10;
		int b = 20;
		
		System.out.println("maximum from a and b is : " + Math.max(a, b));
		System.out.println("minimum from a and b is : " + Math.min(a, b));
		System.out.println("Exp : " + Math.exp(2));
		System.out.println("random number : " + Math.random() * 100);
		System.out.println("Square Root : " + Math.sqrt(2));

	}
}
