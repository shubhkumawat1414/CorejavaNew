package Operators;

public class UnaryOperators {
	public static void main(String[] args) {
	int i=0;
	int c = 2+ i++;
	System.out.println("value of c when post increment operator apply : "+c);
	int j=0;
	int c1 = 2+ ++j;
	System.out.println("value of c1 when pre increment operator apply : "+c1);
	}
	

}
